class Solution:
    def smallerNumbersThanCurrent(self, nums: List[int]) -> List[int]:
        
        sort_nums = sorted(nums)
        res = []
        for i in nums:
            res.append(sort_nums.index(i))
        return res
    
            
            
        
       
     

        
        
        
        
        
       
        
        
       
    
            
        
        
        
        
    
    
    
        
        
        
        
             
                    
                    
            
            
        
        
        
        
        
        
       
        